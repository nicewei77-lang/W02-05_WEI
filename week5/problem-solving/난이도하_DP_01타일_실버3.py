"""
문제
지원이에게 2진 수열을 가르쳐 주기 위해, 지원이 아버지는 그에게 타일들을 선물해주셨다. 그리고 이 각각의 타일들은 0 또는 1이 쓰여 있는 낱장의 타일들이다.
어느 날 짓궂은 동주가 지원이의 공부를 방해하기 위해 0이 쓰여진 낱장의 타일들을 붙여서 한 쌍으로 이루어진 00 타일들을 만들었다. 결국 현재 1 하나만으로 이루어진 타일 또는 0타일을 두 개 붙인 한 쌍의 00타일들만이 남게 되었다.
그러므로 지원이는 타일로 더 이상 크기가 N인 모든 2진 수열을 만들 수 없게 되었다. 예를 들어, N=1일 때 1만 만들 수 있고, N=2일 때는 00, 11을 만들 수 있다. (01, 10은 만들 수 없게 되었다.) 또한 N=4일 때는 0011, 0000, 1001, 1100, 1111 등 총 5개의 2진 수열을 만들 수 있다.
우리의 목표는 N이 주어졌을 때 지원이가 만들 수 있는 모든 가짓수를 세는 것이다. 단 타일들은 무한히 많은 것으로 가정하자.

입력
첫 번째 줄에 자연수 N이 주어진다. (1 ≤ N ≤ 1,000,000)

출력
첫 번째 줄에 지원이가 만들 수 있는 길이가 N인 모든 2진 수열의 개수를 15746으로 나눈 나머지를 출력한다.

예제 입력 1 
4
예제 출력 1 
5
"""
# 00, 1 타일로 만들 수 있는 길이 N의 모든 타일 배치 경우의 수 = memo[N]

# N = int(input())
N = 100000

prev2 = 1
prev1 = 2

for i in range(3, N+1):
    current = prev2 + prev1
    prev2 = prev1
    prev1 = current
    
print(current % 15746)

N = int(input())

# base case
if N == 1:
    print(1)
elif N == 2:
    print(2)
# 슬라이딩 윈도우(고정 크기 배열을 한 칸씩 밀면서 값 처리)
# 마지막 칸에 타일을 놓는 경우 1 혹은 00 - 1을 놓는 경우는 f(n-1)과 같고 00을 놓는 경우는 f(n-2)와 같다 - 이 두 가지 경우는 겹치지 않음(길이, 마지막에 올 타일 종류가 다름) - 그러므로 둘을 더하면 모든 경우의 수가 나온다
else:
# i=3부터 prev2, prev1, current는 모두 % 15746 을 취한 나머지값
# 나머지끼리 더해도 괜찮은 이유 (모듈러 덧셈 법칙):
#   나머지A = 실제A - m×q1
#   나머지B = 실제B - m×q2
#   → (나머지A + 나머지B) % m
#     = ((실제A + 실제B) - m×(q1+q2)) % m
#     = (실제A + 실제B) % m
# 즉, m의 배수는 나머지 계산에서 사라지므로
# 나머지끼리 더한 나머지 == 실제값끼리 더한 나머지
    prev2 = 1
    prev1 = 2
    for i in range(3, N+1):
        current = (prev2 + prev1) % 15746
        prev2 = prev1
        prev1 = current
    print(current)



