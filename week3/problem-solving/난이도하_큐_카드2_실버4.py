# 큐 - 카드2 (백준 실버4)
# 문제 링크: https://www.acmicpc.net/problem/2164

"""
# 문제
N장의 카드가 있다. 각각의 카드는 차례로 1부터 N까지의 번호가 붙어 있으며, 1번 카드가 제일 위에, N번 카드가 제일 아래인 상태로 순서대로 카드가 놓여 있다.

이제 다음과 같은 동작을 카드가 한 장 남을 때까지 반복하게 된다. 우선, 제일 위에 있는 카드를 바닥에 버린다. 그 다음, 제일 위에 있는 카드를 제일 아래에 있는 카드 밑으로 옮긴다.

예를 들어 N=4인 경우를 생각해 보자. 카드는 제일 위에서부터 1234 의 순서로 놓여있다. 1을 버리면 234가 남는다. 여기서 2를 제일 아래로 옮기면 342가 된다. 3을 버리면 42가 되고, 4를 밑으로 옮기면 24가 된다. 마지막으로 2를 버리고 나면, 남는 카드는 4가 된다.

N이 주어졌을 때, 제일 마지막에 남게 되는 카드를 구하는 프로그램을 작성하시오.

# 입력
첫째 줄에 정수 N(1 ≤ N ≤ 500,000)이 주어진다.

# 출력
첫째 줄에 남게 되는 카드의 번호를 출력한다.
"""
# 큐 초기화
from collections import deque
n = int(input())
que = deque(range(1, n+1))

# 카드가 한 장 남을 때까지 위 카드 버리기 - 위 카드 아래로 옮기기
while len(que) > 1:
    que.popleft()
    que.append(que.popleft())
    
# 마지막 카드 구하기
print(que[0])
    
"""
[Python deque & Loop Logic 핵심 요약]

1. 생성: deque(range(1, n+1))
   - 1부터 n까지의 연속된 숫자를 효율적으로 초기화하는 표준 방법.

2. pop() & popleft()의 특성
   - 단순히 요소를 삭제하는 것이 아니라, 값을 '반환(return)'함.
   - 변수에 할당하여 즉시 재사용하거나 다른 곳에 추가 가능.
   - popleft()는 리스트의 pop(0)보다 시간 복잡도 면에서 압도적 우위(O(1)).

3. while len(que) > 1 vs == 1
   - > 1 : "마지막 한 개가 남을 때까지 반복하라" (서바이벌/요세푸스 로직).
   - == 1 : "딱 하나 있을 때만 실행하라" (보통 의도와 다르게 작동 안 할 확률 높음).

4. 활용 팁: 
   - '버리고 다음 장을 뒤로 보내기' 등 데이터를 순환시킬 때 
     que.append(que.popleft()) 조합을 자주 사용함.
"""

   